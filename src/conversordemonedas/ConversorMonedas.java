    public void conversion(double cantidad,double precioMoneda){
            double resultado;
            cantidad = Double.parseDouble(jTextFieldMonedaAconvertir.getText());
            //VALOR DE PESO MEXICANO ES 0.050 FRENTE A LIBRA EXTERLINA A LA FECHA DE CREACION DE ESTE PROYECTO 
            resultado = precioMoneda * cantidad;
            jLabelResultado.setText(""+resultado);
    }
    
