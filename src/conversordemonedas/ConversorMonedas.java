    private void jButtonConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConvertirActionPerformed
        
        if(jTextFieldGradosAConvertir.getText().length()==0){
            
            JOptionPane.showMessageDialog(null,"No ha colocado los grados a convertir");
            
        }else{
        
            if(jComboBoxdGradosAConvertir.getSelectedItem() == jComboBoxConvertirAEste.getSelectedItem()){
                
                JOptionPane.showMessageDialog(null,"No se puede convertir entre la misma");
                
            }else{
                if(jComboBoxdGradosAConvertir.getSelectedItem() == "CELCIUS"){
                    double resultado;
                    double grados = Double.parseDouble(jTextFieldGradosAConvertir.getText());
                    resultado = (grados*1.8)+32;
                    jLabelResultadosGrados.setText(resultado+"° Farenheit");
                }else{
                    double resultado;
                    double grados = Double.parseDouble(jTextFieldGradosAConvertir.getText());
                    resultado = (grados-32)/1.8;
                    jLabelResultadosGrados.setText(resultado+"° Celcius");
                }
                
            }
            
        }
        
    }//GEN-LAST:event_jButtonConvertirActionPerformed

